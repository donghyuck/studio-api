CREATE TABLE TB_PLATFORM_WORKSPACE (
    WORKSPACE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PARENT_ID BIGINT,
    ROOT_ID BIGINT,
    COMPANY_ID BIGINT NOT NULL,
    NAME VARCHAR(255) NOT NULL,
    SLUG VARCHAR(100) NOT NULL,
    PATH VARCHAR(1024) NOT NULL,
    DEPTH INTEGER NOT NULL,
    POSITION INTEGER NOT NULL,
    VISIBILITY VARCHAR(20) NOT NULL,
    ARCHIVED BOOLEAN NOT NULL,
    ARCHIVED_AT TIMESTAMP,
    ARCHIVED_BY BIGINT,
    CREATED_BY BIGINT NOT NULL,
    UPDATED_BY BIGINT NOT NULL,
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP
);

CREATE UNIQUE INDEX UK_PLATFORM_WORKSPACE_COMPANY_PATH
    ON TB_PLATFORM_WORKSPACE (COMPANY_ID, PATH);

CREATE UNIQUE INDEX UK_PLATFORM_WORKSPACE_COMPANY_PARENT_SLUG
    ON TB_PLATFORM_WORKSPACE (COMPANY_ID, PARENT_ID, SLUG);

CREATE UNIQUE INDEX UK_PLATFORM_WORKSPACE_COMPANY_ROOT_SLUG
    ON TB_PLATFORM_WORKSPACE (COMPANY_ID, SLUG);
